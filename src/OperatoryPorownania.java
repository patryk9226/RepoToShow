public class OperatoryPorownania {

    public static void main(String[] args) {
        int firstNumber = 4;
        int secondNumber = 6;

        boolean resoult = secondNumber > firstNumber;

        System.out.println(resoult);
        System.out.println(firstNumber > secondNumber);
        System.out.println(firstNumber < secondNumber);
        System.out.println(firstNumber >= secondNumber);
        System.out.println(firstNumber <= secondNumber);
        System.out.println(firstNumber == secondNumber);
        System.out.println(firstNumber != secondNumber);
    }
}
